package toto.tarolo;

public class Talalat {
    private final int talalatokSzama;
    private final int nyeremeny;

    public Talalat(int b, int c) {
        talalatokSzama = b;
        nyeremeny = c;
    }

    public int getTalalatokSzama() {
        return talalatokSzama;
    }

    public int getNyeremeny() {
        return nyeremeny;
    }

}
    

